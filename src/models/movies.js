class Movie {
  constructor(
    year,
    votes,
    title,
    runtime,
    revenue,
    rating,
    rank,
    metascore,
    genre,
    director,
    description,
    actors,
  ) {
    this.year = year;
    this.votes = votes;
    this.title = title;
    this.runtime = runtime;
    this.revenue = revenue;
    this.rating = rating;
    this.rank = rank;
    this.metascore = metascore;
    this.genre = genre;
    this.director = director;
    this.description = description;
    this.actors = actors;
  }
}
